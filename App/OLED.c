void LCD_WrDat(byte dat) 
{
byte  i;
DCHIGH(); 
for(i=0;i<8;i++) //发送一个八位数据 
{
if((dat << i) & 0x80)
{MISOHIGH();}
else  MISOLOW();
CLKLOW();
CLKHIGH();
}
}



